#include <stdio.h>

int main() {
    char A = 'A', B = 'B', a = 'a', b = 'b';
    char uno = 1, due = '2', dollaro = '$', star = '*', somma = '+';

    printf("Intero equivalente ad A: %d\n", A); 
    printf("Intero equivalente a B: %d\n", B); 
    printf("Intero equivalente ad a: %d\n", a); 
    printf("Intero equivalente a b: %d\n", b); 
    printf("Intero equivalente ad 1: %d\n", uno); 
    printf("Intero equivalente a 2: %d\n", due); 
    printf("Intero equivalente a $: %d\n", dollaro); 
    printf("Intero equivalente a *: %d\n", star); 
    printf("Intero equivalente a +: %d\n", somma); 
    return 0;
}